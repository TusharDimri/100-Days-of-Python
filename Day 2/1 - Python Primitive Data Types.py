# Data Types:- 

# 1. Strings :-

print("Tushar"[0]) 
#  This method of getting particular element(s) from a string is called subscripting

# Printing the last character of given string:-
print( "Tushar"[ len("Tushar") - 1] ) # "Tushar"[5]

# String Concatenation:-
print("1234"+ "5678")



# 2. Integers :-

print(1234 + 5678)

# 1 Million = 1,000,000. In Python, 1 Million = 1_000_000
print(1_000_000 + 1_000_000) # Underscores are ignored by Python Interpreter



# Float:-

print(3.14 + 2.12)
print(1_000_000.213 + 101.4)


# Boolean:-

print(True)
print(False)